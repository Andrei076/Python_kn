"""Модуль в котором находятся три функции: two_digits, amorphous, digits_next_to
"""


def two_digits():
    """Функция определяет,верно ли, что в записи введеного натурального числа
    есть две одинаковые цифры (не обязательно стоящие рядом)
    """
    x = input("Введите натуральное число: ")
    if x.isdigit():
        n = 0
        for i in range(0, len(x)):
            for j in range(i + 1, len(x)):
                if x[i] == x[j]:
                    n += 1
        if n == 0:
            print('Нет.')
        else:
            print('Да.')
    else:
        print("Это не натуральное число")


