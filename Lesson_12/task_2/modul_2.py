def digits_next_to():
    """Функция определяет,верно ли, что в записи введеного натурального числа
    есть две одинаковые цифры стоящие рядом
    """
    a = input("Введите натуральное число: ")
    if a.isdigit():
        n = 0
        for i in range(0, (len(a)) - 1):
            if a[i] == a[i + 1]:
                n += 1
        if n == 0:
            print('Нет.')
        else:
            print('Да.')
    else:
        print("Это не натуральное число")
